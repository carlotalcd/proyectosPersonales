    public void insertar(){
    tabla1();
    int fila = jTable1.getSelectedRow();
    if (fila >= 0) {
        try {
            // Obtener los nuevos valores de los JTextField
            String idAlimentacion = jTextField1.getText();
            String tipo = jTextField2.getText();
            String precio = jTextField3.getText();
            String caducidad = jTextField4.getText();
            String planta = jTextField5.getText();
            String descuento = jTextField6.getText();
            String marca = jTextField7.getText();

            // Preparar la sentencia SQL UPDATE
            String sql = "UPDATE alimentacion SET ID_Alimentacion=?, Tipo=?, Precio=?, Caducidad=?, Planta=?, Descuento=?, Marca=? WHERE ID_Alimentacion=?";
            PreparedStatement statement = cn.prepareStatement(sql);
            statement.setString(1, idAlimentacion);
            statement.setString(2, tipo);
            statement.setString(3, precio);
            statement.setString(4, caducidad);
            statement.setString(5, planta);
            statement.setString(6, descuento);
            statement.setString(7, marca);
            statement.setString(8, jTable1.getValueAt(fila, 0).toString()); // ID_Alimentacion original

            // Ejecutar la sentencia UPDATE
            int filasActualizadas = statement.executeUpdate();
            
            // Verificar si la actualización fue exitosa
            if (filasActualizadas > 0) {
                JOptionPane.showMessageDialog(null, "La fila se actualizó correctamente.");
            } else {
                JOptionPane.showMessageDialog(null, "Error al actualizar la fila.");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al actualizar la fila: " + ex.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(null, "Fila no seleccionada.");
    }

}


