    private void jButton15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton15ActionPerformed
        
        //Creamos los distintos usuarios para el control de acceso
        ArrayList<Usuario> usuarios = new ArrayList <>();
        
        //Añadimos usuarios con sus respectivas contraseñas
        usuarios.add(new Usuario("Adrian","1234"));
        usuarios.add(new Usuario("Santiago","1234"));
        usuarios.add(new Usuario("Carlota","1234"));
        usuarios.add(new Usuario("David","1234"));
        usuarios.add(new Usuario("Josep","1234"));
        
        //Guardamos la info en estas varibales
        String n = JOptionPane.showInputDialog("Ingresa tu nombre :");
        String c = JOptionPane.showInputDialog("Ingrese la clave :");
        
        boolean accesoPermitido = false;
        
        //Comprueba que el usuario ingresado existe y corresponde con su respectiva contraseña
        for(Usuario usuario:usuarios){
            if (usuario.getNombre().equals(n)&&usuario.getPassword().equals(c)){
                accesoPermitido=true;
                break;
            }  
        }
        
        if (accesoPermitido){
            
            JOptionPane.showMessageDialog(null, "Bienvenido a Personal : "+n);
            pantallaCrud.setVisible(true);
            
            
        }else{
           JOptionPane.showMessageDialog(null, "Datos incorrectos");
        }
        
        //Registra en un fichero de texto la consexión a personal con hora y fecha.
        FileWriter fw;
        
        try {
            fw = new FileWriter("C:\\test\\Registro_Inico_Sesion.txt", true);
            fw.write("\n" +  "Se han conectado a Personal   " + LocalTime.now() + " " + LocalDate.now());
            fw.close();
        } catch (IOException ex) {
            Logger.getLogger(Pantalla.class.getName()).log(Level.SEVERE, null, ex);
        }
            
            
  
   
    }//GEN-LAST:event_jButton15ActionPerformed

